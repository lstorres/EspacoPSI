		<div class="content">			

			O termo “neurose” foi apresentado pela primeira vez em 1769, pelo médico Willian Cullen, para nomear doenças nervosas e distúrbios psicológicos. As consequências dessa inicial conotação persistem: ainda hoje, o senso comum constitui um sentido errôneo ou depreciativo à palavra. 

			A partir de 1893, com Sigmund Freud e o desenvolvimento de sua teoria psicanalítica, a compreensão de neurose, vigente no discurso da época, foi ressignificada. Desde então, a concepção passou a ser localizada em uma estrutura tripartite de noções utilizadas para explicar o funcionamento psíquico dos indivíduos, designando um modo de ser e relacionar-se com seu desejo e suas contradições. Enfim, a mesma foi consolidada como um dos atuais paradigmas da normalidade, sendo aceita socialmente sua forma de sofrimento. 

			A neurose é um mito individual. Seu funcionamento se dá a partir da constituição do sujeito, desde fatos primitivos, ocorridos na infância. Sua atividade é uma resposta a eventos antigos, em constante repetição e reedição, de forma particular e incompreensível ao olhar de terceiros. 

			Nela, há um conflito entre o “eu” e o “id”, fazendo coexistirem as atitudes que contrariam as exigências pulsionais e as que levam em conta a realidade, em um conflito entre desejo e censura. 

			O mecanismo de defesa psíquico a esses embates utilizado pelos neuróticos é o recalque. Este afasta os conteúdos indesejáveis ou conflituosos da consciência, mas os mantém no inconsciente. Os mesmos podem vir à tona, e no caso, é de maneira simbólica, na forma de sintomas, que os escoam. 


			<b>Formas clássicas de neurose: </b>

			É possível descrever três modelos clássicos de neuroses, com suas particulares manifestações e posições em relação ao gozo. Elementos em comum a eles são o fato de haver transferências quanto a terceiros e defesas contra a sexualidade infantil recalcada. Entretanto esses não são os únicos moldes possíveis, assim como não é necessário que a descrição seja totalmente compatível com o indivíduo para que ele se inscreva na estrutura. 


			<b>Neurose obsessiva </b>

			Nesse tipo de neurose, o conflito é expresso por sintomas compulsivos (ideias persistentes, ritos conjuratórios e realização de atos indesejáveis) e pelo modo de pensar caracterizado por ruminações mentais e dúvidas, que levam a inibições de pensamento e ação. 


			<b>Neurose fóbica </b>

			Neste caso, atesta-se a presença de uma fobia, que é a fixação da angústia em um objeto exterior. Assim, o medo do mesmo é desproporcional ao seu perigo real e acarreta em reações incontroláveis do sujeito. 


			<b>Neurose histérica </b>

			O termo histeria é historicamente associado ao sofrimento de pessoas do sexo feminino – não à toa deriva da palavra grega hystera (útero, matriz). Ademais, foi uma noção cara à Freud, inicialmente utilizada para denominar os sofrimentos das mulheres que atendia (europeias, brancas e burguesas). Todavia, atualmente, sabe-se que essa neurose não é exclusiva a pessoas do gênero e classe social, e sim encontrada em todos os segmentos da sociedade. 

			Seus quadros clínicos são variados e da ordem da somatização, conversão e dissociação. 

			Os sintomas corporais são divididos em paroxísticos (crises emocionais teatrais e ataques ou convulsões de aparência epiléptica) e duradouros (paralisias histéricas, contraturas, cegueira, anestesias e globus faríngeo). 


