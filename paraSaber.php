			A depressão é um transtorno do humor doloroso e frequente, que pode comprometer toda a vida familiar, laborativa e social do paciente, apresentando um quadro clínico grave, o qual é chamado doença depressiva, e que ocorre em todas as faixas etárias, sendo que, os jovens começaram a serem melhor estudados.  

			A depressão é uma doença séria. Se estiver deprimido, pode ter sentimentos de tristeza extrema que duram longos períodos. Esses sentimentos são muitas vezes suficientemente severos para interferirem com a sua vida quotidiana, podendo durar não dias mas semanas ou até meses.  

			A depressão é uma doença comum. Cerca de 15% das pessoas terão uma depressão forte em algum período das suas vidas.  

			A depressão é mais comum em mulheres do que em homens, mas isto pode dever-se ao fato de ser mais provável as mulheres procurarem ajuda para os seus sintomas. Esta doença pode desenvolver-se em qualquer idade, até em crianças.  

			A depressão afeta pessoas de formas diferentes e pode causar vários sintomas físicos, psicológicos (mentais) e sociais.  

			A pessoa com depressão pode ficar irritável e passar muito tempo a chorar.  

			Poderá ter dificuldade em tomar decisões e poderá sentir-se inútil, culpada, desesperada e desamparada.  

			Poderá ainda perder o interesse nas coisas de que costumava gostar e querer passar mais tempo sozinha, evitando estar com familiares e amigos.  

			Também poderá ter sintomas físicos como cansaço e dificuldade em dormir.  

			A pessoa com depressão pode perder o apetite, embora algumas pessoas aumentem o peso porque comem para se confortarem.  

			Também poderá perder o interesse em sexo. Em algumas mulheres, a depressão afeta as menstruações.  

			A depressão é diferente de sofrimento. O sofrimento é uma reação normal a situações de perda e manifesta-se por ondas de tristeza.  

			Normalmente, a pessoa aprecia sua vida e tem esperanças para o futuro. Contudo, com a depressão, a pessoa tem um sentimento constante de tristeza e não tem uma noção positiva do futuro. 

			Raramente existe apenas uma causa para a depressão. Normalmente, são várias causas juntas elas desencadeiam a doença. Por exemplo, pode apresentar baixa autoestima depois de uma doença e sofrer um acontecimento angustiante, como um falecimento, levando assim a uma depressão. 

