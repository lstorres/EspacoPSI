			A psicanálise surge na década de 1890, com Sigmund Freud, um médico interessado em achar um tratamento efetivo para pacientes com sintomas neuróticos ou histéricos. Conversando com os pacientes, Freud acreditava que seus problemas se originaram da inaceitação cultural, sendo assim reprimidos seus desejos inconscientes e suas fantasias de natureza sexual.  

			Desde Freud, a psicanálise se desenvolveu de muitas maneiras e, atualmente, há diversas escolas, (Melaine Klein; Winnicott; entre outros). 

			O método básico da Psicanálise é a interpretação da transferência e da resistência com a análise da livre associação. O analisado, numa postura relaxada, é solicitado a dizer tudo o que lhe vem à mente. Sonhos, esperanças, desejos e fantasias são de interesse, como também as experiências vividas nos primeiros anos de vida em família.  

			Geralmente, o analista simplesmente escuta, fazendo comentários somente quando no seu julgamento profissional visualiza uma crescente oportunidade para que o analisante torne consciente os conteúdos reprimidos que são supostos, a partir de suas associações.  

			Escutando o analisado, o analista tenta manter uma atitude empática de neutralidade. Uma postura de não-julgamento, visando a criar um ambiente seguro. 

			O conceito de inconsciente fora usado por Leibniz 200 anos antes de Freud, também sendo usado por Hegel para construir sua dialética hegeliana. 

			A originalidade do conceito de Inconsciente introduzido por Freud deve-se à proposição de uma realidade psíquica, característica dos processos inconscientes. É preciso diferenciar inconsciente, sem consciência, de Inconsciente, conforme elaborado por Freud, que diz respeito a uma instância psíquica basilar na constituição da personalidade. 
